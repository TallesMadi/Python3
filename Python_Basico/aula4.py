#Tipo int e float

"""
int -> Número Inteiro
O tpo int representa qualquer número
positivo ou negativo. 
int sem sinal é considerado positivo
"""
print(11, -11)
print(0)

"""
float -> número com ponto flutuante, decimal.
O tipo float represena qualquer número
positivo ou negativo com ponto flutante.
float sem sinal é considerado positivo
"""
print(11.11, -11.11)
print(0.0)

# a função type mostra o tipo que o python inferiu o valor
print(type('Talles'))
print(type(7))
print(type(7.7))
